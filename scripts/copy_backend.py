def _parse_args(argv=None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        prog="copy_backend.py",
        description="Copy/sync a Spec Server backlog between storage backends "
                    "(postgres <-> dynamodb).",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=_EPILOG,
    )
    parser.add_argument("--source", required=True,
                        choices=["postgres", "dynamodb"],
                        help="source backend to read from")
    parser.add_argument("--dest", required=True,
                        choices=["postgres", "dynamodb"],
                        help="destination backend to write to")
    scope = parser.add_mutually_exclusive_group(required=True)
    scope.add_argument("--project", metavar="SLUG",
                       help="copy a single project by slug")
    scope.add_argument("--all", action="store_true",
                       help="copy every project on the source")
    parser.add_argument("--dry-run", action="store_true",
                        help="read the source only and report what WOULD copy")
    parser.add_argument("-v", "--verbose", action="store_true",
                        help="log per-project progress to stderr")
    return parser.parse_args(argv)


_EPILOG = """\
environment:
  postgres side:  SRC_DATABASE_URL / DEST_DATABASE_URL
  dynamodb side:  SRC_DYNAMODB_TABLE / DEST_DYNAMODB_TABLE          (default spec-server)
                  SRC_DYNAMODB_ENDPOINT_URL / DEST_DYNAMODB_ENDPOINT_URL  (DynamoDB Local)
                  SRC_AWS_REGION / DEST_AWS_REGION                  (default AWS_REGION/us-east-1)
                  AWS creds via the ambient boto3 chain (shared by both dynamo sides)

examples:
  python scripts/copy_backend.py --source postgres --dest dynamodb --project spec-server --dry-run
  python scripts/copy_backend.py --source postgres --dest dynamodb --project spec-server
  python scripts/copy_backend.py --source dynamodb --dest postgres --all

notes:
  * at most one side may be postgres (one global SQLAlchemy session per process);
    a postgres->postgres copy is a pg_dump/pg_restore job and is refused.
  * --dry-run never writes. A real run upserts by key and never deletes; append-only
    notes/events/decisions and keyless tasks are NOT de-duplicated on a re-run.
  * public_id/version/timestamps/owner/lease are regenerated or dropped by the dest
    (see the module docstring for the exact preservation table).
"""


def main(argv=None) -> int:
    args = _parse_args(argv)

    if args.source == "postgres" and args.dest == "postgres":
        sys.exit(
            "error: postgres->postgres is unsupported (single global SQLAlchemy "
            "session per process). Use pg_dump/pg_restore for a Postgres copy."
        )
    if args.source == args.dest:
        print("warning: source and dest backend kinds are identical — ensure "
              "their connection config differs.", file=sys.stderr)

    report = Report(dry_run=args.dry_run)
    src = None
    dest = None
    try:
        src = build_backend("source", args.source)
        # In --dry-run we never write, but we still build the dest so its config
        # is validated up front (cheap, and no writes are ever issued).
        dest = build_backend("dest", args.dest)

        copier = Copier(src, dest, report, verbose=args.verbose)

        if args.project:
            slugs = [args.project]
        else:
            slugs = [p.slug for p in src.impl.list_projects()]

        if not slugs:
            print("no projects found on the source; nothing to do.")
            return 0

        print(f"{'DRY-RUN: ' if args.dry_run else ''}"
              f"copying {len(slugs)} project(s) "
              f"{args.source} -> {args.dest}: {', '.join(slugs)}")

        for slug in slugs:
            try:
                copier.copy_project_all(slug)
            except Exception as exc:  # noqa: BLE001 — one bad project must not abort all
                report.warn(f"project {slug}: aborted ({type(exc).__name__}: {exc})")
                print(f"error: project {slug} aborted: {exc}", file=sys.stderr)

        print()
        print(report.render())
        total_failed = sum(s.failed for s in report.stats.values())
        return 1 if total_failed else 0
    finally:
        # Pop any pushed Flask app context (postgres side).
        for b in (dest, src):
            if b is not None and b.ctx is not None:
                try:
                    b.ctx.pop()
                except Exception:  # noqa: BLE001
                    pass


if __name__ == "__main__":
    raise SystemExit(main())
