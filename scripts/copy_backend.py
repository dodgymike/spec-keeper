#!/usr/bin/env python3
"""copy_backend.py — one-off backlog copy/sync between storage backends (SLS-10).

Move a project's backlog between the two Spec Server storage backends
(PostgreSQL <-> DynamoDB) so a repo can flip ``STORAGE_BACKEND`` without losing
data. This is a **standalone operator tool**, not app runtime code.

Design — reuse the storage layer, no bespoke schema knowledge (mostly)
---------------------------------------------------------------------
The elegant core: instantiate BOTH backends behind the SAME
``app.storage.StorageBackend`` interface and copy entities through the neutral
frozen DTOs (``app/storage/dto.py``). Reading the backlog therefore needs zero
knowledge of either physical schema — ``list_projects`` / ``list_tasks`` / ... do
the enumeration and hand back DTOs; the matching ``create_*`` / ``upsert_*`` /
``add_*`` methods write them to the destination.

Two things the neutral *interface* cannot express, so they use a small, clearly
isolated backend-specific path (the ``_pg_*`` / ``_ddb_*`` helpers):

  * **Numbered resources** — counters have no "set to value N" method (the live
    API only ever *increments*), and reservation rows have no "insert with an
    explicit value" method. We must land these directly so the destination's
    NEXT ``reserve_number`` continues monotonically. We set each counter to the
    source's ``current_value`` (never lowering it).
  * **Relations & chain-runs** — the read interface exposes no ``list_relations``
    / ``list_chain_runs``. We enumerate them per-backend, then WRITE them back
    through the neutral API (``add_relation`` / ``create_chain_run`` +
    ``upsert_chain_step``).

What is preserved, and what is NOT
----------------------------------
Copied faithfully: slugs, human keys, titles, descriptions, status, priority,
component, proof_cmd, section, position, tags, task<->epic links, task<->task
relations, reservation values + counter high-water marks (so numbering keeps
climbing), agent roster, epic/task note *bodies*, commit SHAs, event/decision
*content*.

Cannot be perfectly preserved (the destination generates them, or the neutral
write API resets them) — reported honestly per run:

  * ``public_id`` — regenerated by every ``create_*`` (a fresh UUID on the dest).
  * ``version`` — reset to 1 (``create_task`` starts fresh; optimistic-lock
    history does not survive).
  * timestamps (``created_at`` / ``updated_at`` / ``completed_at`` / note+commit
    times / chain start/finish) — set to copy-time on the destination.
  * ``owner`` / ``lease_expires_at`` — dropped (a mid-flight claim/lease is NOT
    migrated; run the copy against a quiesced source).
  * DB integer ids — internal to Postgres, meaningless across backends.
  * reservation -> task linkage — the ``ReservationDTO`` carries no task ref, so
    the audit row's task association is not reconstructed.

Idempotency & safety
--------------------
  * ``--dry-run`` READS the source only and prints per-entity counts of what
    WOULD be copied. It never touches the destination.
  * A real run is re-runnable: keyed entities (projects/epics/tasks/agents,
    reservations by value, commits by sha, counters by value) are skipped or
    max-merged if they already exist on the destination — never duplicated, and
    nothing is ever DELETED from either side.
  * CAVEAT: append-only rows with no natural key — epic/task **notes**,
    **events**, **decisions**, and **tasks that have no human key** — cannot be
    de-duplicated on a second run and WILL be appended again. Run once, or run
    into a fresh destination. This is surfaced in the final report.

Process/coexistence constraint
------------------------------
``PostgresBackend`` uses Flask-SQLAlchemy's single global ``db.session`` (one
engine per process), so **at most one side may be postgres**. That covers the
real use case — a backend switch is postgres<->dynamodb. A postgres->postgres
copy is a job for ``pg_dump``/``pg_restore``; this tool refuses it.

Configuration (environment)
---------------------------
Each side reads its own prefixed config so both can be set simultaneously:

  Postgres side (whichever of source/dest is ``postgres``):
    SRC_DATABASE_URL   / DEST_DATABASE_URL
        e.g. postgresql+psycopg://spec:spec@localhost:5432/specserver

  DynamoDB side (whichever of source/dest is ``dynamodb``):
    SRC_DYNAMODB_TABLE / DEST_DYNAMODB_TABLE           (default: spec-server)
    SRC_DYNAMODB_ENDPOINT_URL / DEST_DYNAMODB_ENDPOINT_URL
        set for DynamoDB Local, e.g. http://localhost:8000; unset for real AWS
    SRC_AWS_REGION / DEST_AWS_REGION                   (default: AWS_REGION or us-east-1)

  AWS credentials come from the ambient boto3 default chain
  (AWS_ACCESS_KEY_ID/AWS_SECRET_ACCESS_KEY/AWS_PROFILE). Both DynamoDB sides
  therefore share one credential set — cross-ACCOUNT dynamo->dynamo copies are
  out of scope (run the tool once per account, or use a single profile).

Examples
--------
Postgres -> DynamoDB (one project), preview first:

    export SRC_DATABASE_URL=postgresql+psycopg://spec:spec@localhost:5432/specserver
    export DEST_DYNAMODB_TABLE=spec-server
    export DEST_DYNAMODB_ENDPOINT_URL=http://localhost:8000
    export AWS_ACCESS_KEY_ID=local AWS_SECRET_ACCESS_KEY=local AWS_REGION=us-east-1
    python scripts/copy_backend.py --source postgres --dest dynamodb \
        --project spec-server --dry-run
    python scripts/copy_backend.py --source postgres --dest dynamodb --project spec-server

DynamoDB -> Postgres (all projects):

    export SRC_DYNAMODB_TABLE=spec-server SRC_DYNAMODB_ENDPOINT_URL=http://localhost:8000
    export DEST_DATABASE_URL=postgresql+psycopg://spec:spec@localhost:5432/specserver
    python scripts/copy_backend.py --source dynamodb --dest postgres --all
"""
